--Статистика по пит стопам
WITH team_pit AS (
    SELECT
        t.team_id,
        t.team_name,
        COUNT(ps.pitstop_id) AS total_pitstops,
        COUNT(DISTINCT ps.race_id) AS races_with_stops,
        AVG(ps.stop_time_seconds) AS avg_stop_time,
        MIN(ps.stop_time_seconds) AS fastest_stop_time,
        MAX(ps.stop_time_seconds) AS slowest_stop_time
    FROM f1.teams t
    LEFT JOIN f1.results r ON r.team_id = t.team_id
    LEFT JOIN f1.pit_stops ps ON ps.race_id = r.race_id AND ps.driver_id = r.driver_id
    JOIN f1.races rc ON r.race_id = rc.race_id
    JOIN f1.seasons s ON rc.season_id = s.season_id
    WHERE s.season_year = 2024
    GROUP BY t.team_id, t.team_name
),
team_pit_race AS (
    SELECT
        t.team_id,
        ps.race_id,
        SUM(ps.stop_time_seconds) AS total_time_in_race
    FROM f1.teams t
    JOIN f1.results r ON r.team_id = t.team_id
    JOIN f1.pit_stops ps ON ps.race_id = r.race_id AND ps.driver_id = r.driver_id
    JOIN f1.races rc ON r.race_id = rc.race_id
    JOIN f1.seasons s ON rc.season_id = s.season_id
    WHERE s.season_year = 2024
    GROUP BY t.team_id, ps.race_id
)
SELECT
    tp.team_name,
    tp.total_pitstops,
    tp.races_with_stops,
    ROUND(tp.avg_stop_time, 3) AS avg_stop_time,
    ROUND(tp.fastest_stop_time, 3) AS fastest_stop_time,
    ROUND(tp.slowest_stop_time, 3) AS slowest_stop_time,
    ROUND(tp.total_pitstops::numeric / tp.races_with_stops, 2) AS avg_stops_per_race,
    ROUND(AVG(tr.total_time_in_race), 3) AS avg_total_pitstop_time_per_race,
    RANK() OVER (ORDER BY tp.avg_stop_time) AS speed_rank
FROM team_pit tp
LEFT JOIN team_pit_race tr ON tr.team_id = tp.team_id
GROUP BY
    tp.team_id,
    tp.team_name,
    tp.total_pitstops,
    tp.races_with_stops,
    tp.avg_stop_time,
    tp.fastest_stop_time,
    tp.slowest_stop_time
ORDER BY tp.avg_stop_time
LIMIT 5;

--Сравнение первого и второго гонщика команды
SELECT
    t.team_name,
    td.driver_position,
    d.driver_name,
    ROUND(AVG(r.finish_position), 2) AS avg_finish_pos,
    COUNT(r.result_id) AS races,
    SUM(r.points) AS total_points,
    COUNT(CASE WHEN r.finish_position = 1 THEN 1 END) AS wins,
    COUNT(CASE WHEN r.finish_position <= 3 THEN 1 END) AS podiums
FROM f1.team_drivers td
JOIN f1.drivers d ON td.driver_id = d.driver_id
JOIN f1.teams t ON td.team_id = t.team_id
LEFT JOIN f1.results r ON r.driver_id = td.driver_id
WHERE td.season_id = (SELECT season_id FROM f1.seasons WHERE season_year = 2024)
	AND td.driver_position IN (1, 2)
GROUP BY t.team_name, td.driver_position, d.driver_name
ORDER BY t.team_name, td.driver_position;

-- Сравнение квалификации и гонки
SELECT 
	d.driver_id,
    d.driver_name,
    d.driver_number,
    t.team_name,
    COUNT(r.result_id) as races_participated,
    ROUND(AVG(q.grid_position), 2) AS avg_qual_position,
    ROUND(AVG(r.finish_position),2) as avg_race_position,
    ROUND(AVG(q.grid_position - r.finish_position), 2) as avg_position_diff,
    SUM(CASE WHEN q.grid_position > r.finish_position THEN 1 ELSE 0 END) as races_improved,
    SUM(CASE WHEN q.grid_position < r.finish_position THEN 1 ELSE 0 END) as races_worsened,
    SUM(CASE WHEN q.grid_position = r.finish_position THEN 1 ELSE 0 END) as races_same,
    MAX(q.grid_position - r.finish_position) as best_improvement,
    MIN(q.grid_position - r.finish_position) as worst_performance
FROM f1.results r
JOIN f1.drivers d ON r.driver_id = d.driver_id
JOIN f1.teams t ON r.team_id = t.team_id
LEFT JOIN f1.qualifying q ON r.race_id = q.race_id AND r.driver_id = q.driver_id
JOIN f1.races rc ON r.race_id = rc.race_id
JOIN f1.seasons s ON rc.season_id = s.season_id
WHERE s.season_year = 2024
GROUP BY d.driver_id, d.driver_name, d.driver_number, t.team_name
ORDER BY avg_position_diff DESC;

--ТОП 5 гонщиков по набранным очкам в секторах по 5 гонок(1-5,6-10,11-15,16-20,21-24)
WITH race_groups AS (
    SELECT
        r.race_id,
        r.race_number,
        CEIL(r.race_number::numeric / 5) AS race_block
    FROM f1.races r
    JOIN f1.seasons s ON r.season_id = s.season_id
    WHERE s.season_year = 2024
),
points_per_block AS (
    SELECT
        d.driver_id,
        d.driver_name,
        rg.race_block,
        SUM(res.points) AS total_points
    FROM f1.results res
    JOIN f1.drivers d ON res.driver_id = d.driver_id
    JOIN race_groups rg ON res.race_id = rg.race_id
    GROUP BY d.driver_id, d.driver_name, rg.race_block
),
ranked_points AS (
    SELECT
        race_block,
        driver_name,
        total_points,
        RANK() OVER (PARTITION BY race_block ORDER BY total_points DESC) AS rank_in_block
    FROM points_per_block
)
SELECT
    race_block,
    driver_name,
    total_points,
    rank_in_block
FROM ranked_points
WHERE rank_in_block <= 5
ORDER BY race_block, rank_in_block;
