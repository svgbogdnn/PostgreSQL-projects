/*
This PostgreSQL service models a Formula 1 season analytics 
database, storing drivers, teams, circuits, races and seasons 
plus lap records, qualifying results, race results with points,
and pit-stop data to support standings and performance queries.
*/

CREATE SCHEMA IF NOT EXISTS f1;

CREATE TABLE IF NOT EXISTS f1.drivers (
    driver_id SERIAL PRIMARY KEY,
    driver_name VARCHAR(100) NOT NULL,
    driver_number INTEGER NOT NULL,
    championships INTEGER NOT NULL DEFAULT 0,
    UNIQUE(driver_number)
);

CREATE TABLE IF NOT EXISTS f1.teams (
    team_id SERIAL PRIMARY KEY,
    team_name VARCHAR(100) NOT NULL,
    country VARCHAR(50) NOT NULL DEFAULT '',
    championships INTEGER NOT NULL DEFAULT 0,
    UNIQUE(team_name)
);

CREATE TABLE IF NOT EXISTS f1.circuits (
    circuit_id SERIAL PRIMARY KEY,
    circuit_name VARCHAR(100) NOT NULL,
    country VARCHAR(50) NOT NULL DEFAULT '',
    length_km DECIMAL(5, 2) NOT NULL DEFAULT 0.00,
    UNIQUE(circuit_name)
);

CREATE TABLE IF NOT EXISTS f1.lap_records (
    record_id SERIAL PRIMARY KEY,
    circuit_id INTEGER NOT NULL REFERENCES f1.circuits(circuit_id),
    driver_id INTEGER NOT NULL REFERENCES f1.drivers(driver_id),
    lap_time VARCHAR(20) NOT NULL,
    year_set INTEGER NOT NULL DEFAULT 0,
    UNIQUE(circuit_id)
);

CREATE TABLE IF NOT EXISTS f1.seasons (
    season_id SERIAL PRIMARY KEY,
    season_year INTEGER NOT NULL,
    total_races INTEGER NOT NULL DEFAULT 0,
    champion_driver_id INTEGER REFERENCES f1.drivers(driver_id),
    champion_team_id INTEGER REFERENCES f1.teams(team_id),
    UNIQUE(season_year)
);

CREATE TABLE IF NOT EXISTS f1.races (
    race_id SERIAL PRIMARY KEY,
    season_id INTEGER NOT NULL REFERENCES f1.seasons(season_id),
    race_number INTEGER NOT NULL ,
    race_name VARCHAR(100) NOT NULL,
    circuit_id INTEGER NOT NULL REFERENCES f1.circuits(circuit_id),
    race_date DATE NOT NULL,
    laps INTEGER NOT NULL DEFAULT 0,
    UNIQUE(season_id, race_number)
);

CREATE TABLE IF NOT EXISTS f1.team_drivers (
    team_driver_id SERIAL PRIMARY KEY,
    team_id INTEGER NOT NULL REFERENCES f1.teams(team_id),
    driver_id INTEGER NOT NULL REFERENCES f1.drivers(driver_id),
    season_id INTEGER NOT NULL REFERENCES f1.seasons(season_id),
    driver_position INTEGER NOT NULL DEFAULT 0,
    UNIQUE(team_id, driver_id, season_id)
);

CREATE TABLE IF NOT EXISTS f1.results (
    result_id SERIAL PRIMARY KEY,
    race_id INTEGER NOT NULL REFERENCES f1.races(race_id),
    driver_id INTEGER NOT NULL REFERENCES f1.drivers(driver_id),
    team_id INTEGER NOT NULL REFERENCES f1.teams(team_id),
    grid_position INTEGER NOT NULL DEFAULT 0,
    finish_position INTEGER NOT NULL DEFAULT 0,
    points INTEGER NOT NULL DEFAULT 0,
    best_lap_time VARCHAR(20) NOT NULL,
    UNIQUE(race_id, driver_id)
);

CREATE TABLE IF NOT EXISTS f1.pit_stops (
    pitstop_id SERIAL PRIMARY KEY,
    race_id INTEGER NOT NULL REFERENCES f1.races(race_id),
    driver_id INTEGER NOT NULL REFERENCES f1.drivers(driver_id),
    lap_number INTEGER NOT NULL DEFAULT 0,
    stop_time_seconds DECIMAL(5, 2) NOT NULL DEFAULT 0.00
);

CREATE TABLE IF NOT EXISTS f1.qualifying (
    qualifying_id SERIAL PRIMARY KEY,
    race_id INTEGER NOT NULL REFERENCES f1.races(race_id),
    driver_id INTEGER NOT NULL REFERENCES f1.drivers(driver_id),
    grid_position INTEGER NOT NULL DEFAULT 0,
    best_time VARCHAR(20) NOT NULL,
    UNIQUE(race_id, driver_id)
);

